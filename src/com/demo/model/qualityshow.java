package com.demo.model;

public class qualityshow {

	int year;
	int qua;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getQua() {
		return qua;
	}

	public void setQua(int qua) {
		this.qua = qua;
	}

	public qualityshow(int year, int qua) {
		super();
		this.year = year;
		this.qua = qua;
	}

	public qualityshow() {
		super();
	}

}
